def summarize_mode_rows(
    rows: Sequence[ModeValidationRow],
    *,
    mean_gate_m: float = 0.25e-3,
    maximum_gate_m: float = 0.5e-3,
) -> ModeValidationSummary:
    """Aggregate completed-row errors and apply the conservative validation gate."""

    if not rows:
        raise ValueError("rows must not be empty")
    mode = rows[0].mode_name
    mapping = rows[0].mapping_name
    mesh_size = rows[0].mesh_size_m
    if any(
        item.mode_name != mode
        or item.mapping_name != mapping
        or not np.isclose(item.mesh_size_m, mesh_size)
        for item in rows
    ):
        raise ValueError("summary rows must share one mode, mapping, and mesh size")
    errors = np.asarray(
        [match.distance_m for row in rows for match in row.matches],
        dtype=float,
    )
    if errors.size:
        mean = float(np.mean(errors))
        median = float(np.median(errors))
        maximum = float(np.max(errors))
        percentile = float(np.percentile(errors, 95.0))
    else:
        mean = median = maximum = percentile = float("nan")
    completed = sum(item.completed for item in rows)
    exact = sum(item.exactly_three_topology for item in rows)
    gate = bool(
        completed == len(rows)
        and exact == len(rows)
        and np.isfinite(mean)
        and mean <= mean_gate_m
        and maximum <= maximum_gate_m
    )
    return ModeValidationSummary(
        mode_name=mode,
        mapping_name=mapping,
        mesh_size_m=mesh_size,
        selected_rows=len(rows),
        completed_rows=completed,
        exactly_three_rows=exact,
        matched_minima=int(errors.size),
        mean_error_m=mean,
        median_error_m=median,
        maximum_error_m=maximum,
        percentile_95_error_m=percentile,
        selected_interpolation_sensitive=sum(
            item.selected_interpolation_sensitive for item in rows
        ),
        rejected_candidates=sum(item.rejected_candidates for item in rows),
        validation_gate_passed=gate,
    )


def track_mesh_branches(
    rows: Sequence[ModeValidationRow],
    *,
    stability_tolerance_m: float = 0.25e-3,
) -> tuple[MeshBranchRecord, ...]:
    """Assignment-track robust minima through successive mesh refinements."""

    if not np.isfinite(stability_tolerance_m) or stability_tolerance_m <= 0.0:
        raise ValueError("stability_tolerance_m must be finite and positive")
    by_row: dict[int, list[ModeValidationRow]] = defaultdict(list)
    for row in rows:
        if row.completed:
            by_row[row.row_number].append(row)
    records = []
    for row_number, cases in sorted(by_row.items()):
        ordered = sorted(cases, key=lambda item: item.mesh_size_m, reverse=True)
        if len(ordered) < 2:
            continue
        branch_positions = ordered[0].computed_positions_m.copy()
        previous = ordered[0]
        for current in ordered[1:]:
            matches = match_minima_by_distance(
                branch_positions,
                current.computed_positions_m,
            )
            reordered = np.empty_like(branch_positions)
            for match in matches:
                branch_index = match.reference_index - 1
                reordered[branch_index] = match.computed_position_m
                records.append(
                    MeshBranchRecord(
                        row_number=row_number,
                        branch_index=match.reference_index,
                        coarse_mesh_size_m=previous.mesh_size_m,
                        fine_mesh_size_m=current.mesh_size_m,
                        coarse_position_m=match.reference_position_m.copy(),
                        fine_position_m=match.computed_position_m.copy(),
                        shift_m=match.distance_m,
                        stable=match.distance_m <= stability_tolerance_m,
                        coarse_topology_candidates=previous.topology_candidate_count,
                        fine_topology_candidates=current.topology_candidate_count,
                    )
                )
            branch_positions = reordered
            previous = current
    return tuple(records)


def run_milestone_8_study(
    dataset: ReferenceDataset,
    *,
    row_numbers: Sequence[int] = tuple(range(1, 11)),
    mesh_sizes_m: Sequence[float] = (2.0e-3, 1.5e-3, 1.0e-3, 0.75e-3),
    include_half_mm_rows: Sequence[int] = (1, 2, 3),
    controls: RobustMinimaConfig | None = None,
) -> Milestone8Study:
    """Run identity and best-mapping validation plus robust branch tracking."""

    started = time.perf_counter()
    selected_rows = tuple(int(item) for item in row_numbers)
    if not selected_rows or len(set(selected_rows)) != len(selected_rows):
        raise ValueError("row_numbers must be nonempty and unique")
    robust_controls = controls or RobustMinimaConfig()
    identity = ReferenceValidationVariant(name="identity")
    best = ReferenceValidationVariant(
        name="perm1324",
        electrode_permutation=(1, 3, 2, 4),
    )
    solve_records: list[SolveRecord] = []
    mode_rows: list[ModeValidationRow] = []

    identity_solves = _run_solve_group(
        dataset,
        selected_rows,
        2.0e-3,
        identity,
        robust_controls,
    )
    solve_records.extend(identity_solves)
    for solve in identity_solves:
        mode_rows.extend(_identity_mode_rows(solve))

    best_mesh_solves: dict[float, tuple[SolveRecord, ...]] = {}
    for mesh_size in mesh_sizes_m:
        solves = _run_solve_group(
            dataset,
            selected_rows,
            float(mesh_size),
            best,
            robust_controls,
        )
        best_mesh_solves[float(mesh_size)] = solves
        solve_records.extend(solves)
        for solve in solves:
            robust_row = _mode_row(
                solve,
                "robust",
                "robust-perm1324",
                legacy_audit=False,
            )
            mode_rows.append(robust_row)
            if np.isclose(mesh_size, 2.0e-3):
                mode_rows.append(
                    _mode_row(
                        solve,
                        "recovered-gradient",
                        "old-recovered-perm1324-comparison",
                        legacy_audit=False,
                    )
                )
                mode_rows.append(
                    _mode_row(
                        solve,
                        "recovered-gradient",
                        "audit-flag-perm1324",
                        legacy_audit=True,
                    )
                )
    half_rows = tuple(int(item) for item in include_half_mm_rows)
    if half_rows:
        half_solves = _run_solve_group(
            dataset,
            half_rows,
            0.5e-3,
            best,
            robust_controls,
        )
        best_mesh_solves[0.5e-3] = half_solves
        solve_records.extend(half_solves)
        mode_rows.extend(
            _mode_row(solve, "robust", "robust-perm1324", legacy_audit=False)
            for solve in half_solves
        )

    summary_groups: dict[tuple[str, str, float], list[ModeValidationRow]] = defaultdict(list)
    for row in mode_rows:
        summary_groups[(row.mode_name, row.mapping_name, row.mesh_size_m)].append(row)
    summaries = tuple(
        summarize_mode_rows(rows)
        for _, rows in sorted(summary_groups.items(), key=lambda item: item[0])
    )
    branch_rows = [
        row
        for row in mode_rows
        if row.mode_name == "robust-perm1324"
    ]
    branches = track_mesh_branches(branch_rows)
    return Milestone8Study(
        controls=robust_controls,
        mode_rows=tuple(mode_rows),
        summaries=summaries,
        solve_records=tuple(solve_records),
        branch_records=branches,
        runtime_seconds=time.perf_counter() - started,
    )


