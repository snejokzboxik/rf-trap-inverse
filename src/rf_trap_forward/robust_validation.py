def _run_solve_group(
    dataset: ReferenceDataset,
    row_numbers: Sequence[int],
    mesh_size_m: float,
    variant: ReferenceValidationVariant,
    controls: RobustMinimaConfig,
) -> tuple[SolveRecord, ...]:
    config = real_scale_forward_config(mesh_size_m=mesh_size_m)
    records = []
    for row_number in row_numbers:
        index = row_number - 1
        solver_displacements, reference, row_config = prepare_reference_row_inputs(
            dataset.raw_displacements_m[index],
            dataset.raw_minima_absolute_m[index],
            config,
            variant,
        )
        outcome = _run_worker(solver_displacements, row_config, controls)
        records.append(
            SolveRecord(
                mapping_name=variant.name,
                mesh_size_m=mesh_size_m,
                row_number=row_number,
                reference_positions_m=reference.copy(),
                outcome=outcome,
            )
        )
    return tuple(records)


def _run_worker(
    displacements_m: NDArray[np.float64],
    config: object,
    controls: RobustMinimaConfig,
) -> dict[str, object]:
    completed = subprocess.run(
        [sys.executable, "-m", "rf_trap_forward._minima_mode_worker"],
        input=pickle.dumps((displacements_m, config, controls)),
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if completed.returncode != 0:
        return {
            "ok": False,
            "error_type": "WorkerProcessError",
            "error_message": completed.stderr.decode("utf-8", errors="replace").strip(),
        }
    try:
        outcome = pickle.loads(completed.stdout)
    except Exception as error:
        return {
            "ok": False,
            "error_type": "WorkerProtocolError",
            "error_message": str(error),
        }
    if not isinstance(outcome, dict) or "ok" not in outcome:
        return {
            "ok": False,
            "error_type": "WorkerProtocolError",
            "error_message": "worker returned an invalid object",
        }
    return outcome


def _identity_mode_rows(solve: SolveRecord) -> tuple[ModeValidationRow, ...]:
    old = _mode_row(
        solve,
        "recovered-gradient",
        "old-recovered-identity",
        legacy_audit=False,
    )
    audit = _mode_row(
        solve,
        "recovered-gradient",
        "audit-flag-identity",
        legacy_audit=True,
    )
    robust = _mode_row(
        solve,
        "robust",
        "robust-identity",
        legacy_audit=False,
    )
    return old, audit, robust


def _mode_row(
    solve: SolveRecord,
    worker_mode: str,
    mode_name: str,
    *,
    legacy_audit: bool,
) -> ModeValidationRow:
    outcome = solve.outcome
    empty = np.empty((0, 2), dtype=float)
    common = {
        "mode_name": mode_name,
        "mapping_name": solve.mapping_name,
        "mesh_size_m": solve.mesh_size_m,
        "row_number": solve.row_number,
        "reference_positions_m": solve.reference_positions_m.copy(),
    }
    if not outcome.get("ok"):
        return ModeValidationRow(
            **common,
            status="fem-failed",
            computed_positions_m=empty,
            matches=(),
            topology_candidate_count=0,
            selected_interpolation_sensitive=0,
            rejected_candidates=0,
            total_candidates=0,
            node_count=0,
            triangle_count=0,
            relative_free_residual=float("nan"),
            runtime_seconds=0.0,
            error_type=str(outcome.get("error_type", "unknown")),
            error_message=str(outcome.get("error_message", "unknown")),
        )
    modes = outcome["modes"]
    if worker_mode not in modes:
        error_type, error_message = outcome.get("mode_errors", {}).get(
            worker_mode,
            ("ModeMissing", "worker did not return the requested mode"),
        )
        return ModeValidationRow(
            **common,
            status="mode-failed",
            computed_positions_m=empty,
            matches=(),
            topology_candidate_count=0,
            selected_interpolation_sensitive=0,
            rejected_candidates=0,
            total_candidates=0,
            node_count=int(outcome["node_count"]),
            triangle_count=int(outcome["triangle_count"]),
            relative_free_residual=float(outcome["relative_free_residual"]),
            runtime_seconds=float(outcome["runtime_seconds"]),
            error_type=str(error_type),
            error_message=str(error_message),
        )
    result: MinimaModeResult = modes[worker_mode]
    computed = _positions(result.minima)
    matches = (
        match_minima_by_distance(solve.reference_positions_m, computed)
        if computed.shape == (3, 2)
        else ()
    )
    legacy = result.legacy_diagnostics
    topology = (
        result.accepted_candidates
        if worker_mode == "robust"
        else (legacy.hessian_validated_candidates if legacy is not None else 0)
    )
    if legacy_audit:
        flagged = sum(
            item.selected and item.legacy_interpolation_flag
            for item in result.candidates
        )
    elif worker_mode == "robust":
        flagged = result.selected_interpolation_sensitive
    else:
        flagged = 0
    return ModeValidationRow(
        **common,
        status="ok" if len(matches) == 3 else "insufficient-minima",
        computed_positions_m=computed,
        matches=tuple(matches),
        topology_candidate_count=topology,
        selected_interpolation_sensitive=flagged,
        rejected_candidates=result.rejected_candidates if worker_mode == "robust" else 0,
        total_candidates=len(result.candidates),
        node_count=int(outcome["node_count"]),
        triangle_count=int(outcome["triangle_count"]),
        relative_free_residual=float(outcome["relative_free_residual"]),
        runtime_seconds=float(outcome["runtime_seconds"]),
    )


def _positions(minima: Sequence[LocalMinimum]) -> NDArray[np.float64]:
    if not minima:
        return np.empty((0, 2), dtype=float)
    return np.vstack([item.position_m for item in minima])


