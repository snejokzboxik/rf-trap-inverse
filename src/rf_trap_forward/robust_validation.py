def _scatter_candidates(axis: object, candidates: Sequence[CandidateQualityMetrics]) -> None:
    accepted = [item for item in candidates if item.robust_accepted]
    rejected = [item for item in candidates if not item.robust_accepted]
    if accepted:
        points = 1.0e3 * np.vstack([item.position_m for item in accepted])
        axis.scatter(points[:, 0], points[:, 1], marker="o", color="#16A34A", label="accepted")
    if rejected:
        points = 1.0e3 * np.vstack([item.position_m for item in rejected])
        axis.scatter(points[:, 0], points[:, 1], marker="x", color="#DC2626", label="rejected")
    selected = [item for item in candidates if item.selected]
    if selected:
        points = 1.0e3 * np.vstack([item.position_m for item in selected])
        axis.scatter(points[:, 0], points[:, 1], marker="s", s=55, facecolors="none", edgecolors="black", label="selected")
    for item in candidates:
        axis.annotate(str(item.candidate_id), 1.0e3 * item.position_m, fontsize=8)


def _find_solve(
    study: Milestone8Study,
    mapping: str,
    mesh_size_m: float,
    row_number: int,
) -> SolveRecord:
    return next(
        item
        for item in study.solve_records
        if item.mapping_name == mapping
        and np.isclose(item.mesh_size_m, mesh_size_m)
        and item.row_number == row_number
    )


def _summary_mode_rows(study: Milestone8Study, mode_name: str) -> list[ModeValidationRow]:
    return sorted(
        [
            item
            for item in study.mode_rows
            if item.mode_name == mode_name and np.isclose(item.mesh_size_m, 2.0e-3)
        ],
        key=lambda item: item.row_number,
    )


def _parse_mesh_sizes_mm(value: str) -> tuple[float, ...]:
    values = tuple(float(item.strip()) * 1.0e-3 for item in value.split(",") if item.strip())
    if not values or any(not np.isfinite(item) or item <= 0.0 for item in values):
        raise argparse.ArgumentTypeError("mesh sizes must be positive comma-separated millimetres")
    return values


def build_parser() -> argparse.ArgumentParser:
    """Build the command-line parser for the robust-minima study."""

    parser = argparse.ArgumentParser(
        prog="rf-trap-robust-minima-validation",
        description="Compare legacy, audit-flag, and robust minima post-processing.",
    )
    parser.add_argument("input", nargs="?", default="Data.txt")
    parser.add_argument(
        "--output-directory",
        default="validation_results/milestone_8",
    )
    parser.add_argument(
        "--mesh-sizes-mm",
        type=_parse_mesh_sizes_mm,
        default=(2.0e-3, 1.5e-3, 1.0e-3, 0.75e-3),
    )
    parser.add_argument("--skip-half-mm", action="store_true")
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    """Run the production Milestone 8 study and print its summary."""

    arguments = build_parser().parse_args(argv)
    dataset = load_reference_dataset(arguments.input)
    study = run_milestone_8_study(
        dataset,
        mesh_sizes_m=arguments.mesh_sizes_mm,
        include_half_mm_rows=() if arguments.skip_half_mm else (1, 2, 3),
    )
    paths = write_milestone_8_outputs(study, arguments.output_directory)
    for summary in study.summaries:
        print(
            f"{summary.mode_name}@{1e3 * summary.mesh_size_m:g}mm: "
            f"completed={summary.completed_rows}/{summary.selected_rows}, "
            f"exactly_three={summary.exactly_three_rows}/{summary.selected_rows}, "
            f"mean_mm={1e3 * summary.mean_error_m:.6g}, "
            f"max_mm={1e3 * summary.maximum_error_m:.6g}, "
            f"selected_flags={summary.selected_interpolation_sensitive}, "
            f"rejected={summary.rejected_candidates}, "
            f"gate={'pass' if summary.validation_gate_passed else 'fail'}"
        )
    print(f"report={paths.report_markdown}")
    print(f"runtime_seconds={study.runtime_seconds:.3f}")
    return 0


