def _plot_mesh_branches(study: Milestone8Study, path: Path) -> None:
    figure = Figure(figsize=(10.0, 8.0), constrained_layout=True)
    axes = figure.subplots(2, 2)
    rows = [
        item
        for item in study.mode_rows
        if item.mode_name == "robust-perm1324" and item.completed
    ]
    for axis, row_number in zip(axes.ravel(), (3, 4, 5, 10), strict=True):
        cases = sorted(
            [item for item in rows if item.row_number == row_number],
            key=lambda item: item.mesh_size_m,
            reverse=True,
        )
        if not cases:
            axis.set_title(f"Row {row_number}: no completed cases")
            continue
        branches = cases[0].computed_positions_m.copy()
        histories = [[point.copy()] for point in branches]
        mesh_history = [[cases[0].mesh_size_m] for _ in range(3)]
        for case in cases[1:]:
            matches = match_minima_by_distance(branches, case.computed_positions_m)
            reordered = np.empty_like(branches)
            for match in matches:
                branch_index = match.reference_index - 1
                reordered[branch_index] = match.computed_position_m
                histories[branch_index].append(match.computed_position_m.copy())
                mesh_history[branch_index].append(case.mesh_size_m)
            branches = reordered
        for branch_index, history in enumerate(histories, start=1):
            points = 1.0e3 * np.vstack(history)
            axis.plot(points[:, 0], points[:, 1], "o-", label=f"branch {branch_index}")
            sizes = mesh_history[branch_index - 1]
            axis.annotate(
                f"{1e3 * sizes[0]:g} mm",
                points[0],
                xytext=(4, 5),
                textcoords="offset points",
                fontsize=7,
            )
            axis.annotate(
                f"{1e3 * sizes[-1]:g} mm",
                points[-1],
                xytext=(4, -10),
                textcoords="offset points",
                fontsize=7,
            )
        reference = 1.0e3 * cases[0].reference_positions_m
        axis.scatter(reference[:, 0], reference[:, 1], marker="*", s=75, color="black", label="reference")
        axis.set_title(f"Row {row_number}")
        axis.set_xlabel("x (mm)")
        axis.set_ylabel("y (mm)")
        axis.set_aspect("equal", adjustable="box")
        axis.grid(alpha=0.2)
    handles, labels = axes.ravel()[0].get_legend_handles_labels()
    figure.legend(handles, labels, loc="outside lower center", ncol=4)
    figure.suptitle("Robust branch tracking; endpoint annotations are mesh size h")
    FigureCanvasAgg(figure).print_png(path)


