@dataclass(frozen=True)
class ModeValidationRow:
    """One row-level comparison for one explicit minima-selection mode."""

    mode_name: str
    mapping_name: str
    mesh_size_m: float
    row_number: int
    status: str
    reference_positions_m: NDArray[np.float64]
    computed_positions_m: NDArray[np.float64]
    matches: tuple[MinimumMatch, ...]
    topology_candidate_count: int
    selected_interpolation_sensitive: int
    rejected_candidates: int
    total_candidates: int
    node_count: int
    triangle_count: int
    relative_free_residual: float
    runtime_seconds: float
    error_type: str = ""
    error_message: str = ""

    @property
    def completed(self) -> bool:
        """Return whether exactly three computed minima were assignment-matched."""

        return self.status == "ok" and len(self.matches) == 3

    @property
    def exactly_three_topology(self) -> bool:
        """Return whether the mode had exactly three pre-selection candidates."""

        return self.completed and self.topology_candidate_count == 3

    def errors_m(self) -> NDArray[np.float64]:
        """Return assignment errors as a one-dimensional SI array."""

        return np.asarray([item.distance_m for item in self.matches], dtype=float)


@dataclass(frozen=True)
class ModeValidationSummary:
    """Aggregate reference metrics and artifact counts for one mode."""

    mode_name: str
    mapping_name: str
    mesh_size_m: float
    selected_rows: int
    completed_rows: int
    exactly_three_rows: int
    matched_minima: int
    mean_error_m: float
    median_error_m: float
    maximum_error_m: float
    percentile_95_error_m: float
    selected_interpolation_sensitive: int
    rejected_candidates: int
    validation_gate_passed: bool


@dataclass(frozen=True)
class MeshBranchRecord:
    """One assignment-tracked minimum between successive mesh sizes."""

    row_number: int
    branch_index: int
    coarse_mesh_size_m: float
    fine_mesh_size_m: float
    coarse_position_m: NDArray[np.float64]
    fine_position_m: NDArray[np.float64]
    shift_m: float
    stable: bool
    coarse_topology_candidates: int
    fine_topology_candidates: int


@dataclass(frozen=True)
class SolveRecord:
    """One fresh-process FEM solve and all three post-processing results."""

    mapping_name: str
    mesh_size_m: float
    row_number: int
    reference_positions_m: NDArray[np.float64]
    outcome: dict[str, object]


