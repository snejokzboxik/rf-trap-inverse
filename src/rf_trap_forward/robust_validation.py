def _candidate_records(
    solves: Sequence[SolveRecord],
) -> list[dict[str, object]]:
    records = []
    for solve in solves:
        if not solve.outcome.get("ok"):
            records.append(
                {
                    "mapping": solve.mapping_name,
                    "mesh_size_m": solve.mesh_size_m,
                    "row_number": solve.row_number,
                    "mode": "fem-failed",
                    "status": "failed",
                    "error_type": solve.outcome.get("error_type", "unknown"),
                    "error_message": solve.outcome.get("error_message", "unknown"),
                }
            )
            continue
        modes: dict[str, MinimaModeResult] = solve.outcome["modes"]
        for mode_name, result in sorted(modes.items()):
            for candidate in result.candidates:
                records.append(
                    _candidate_record(
                        solve,
                        mode_name,
                        candidate,
                    )
                )
    return records


def _candidate_record(
    solve: SolveRecord,
    mode_name: str,
    item: CandidateQualityMetrics,
) -> dict[str, object]:
    record: dict[str, object] = {
        "mapping": solve.mapping_name,
        "mesh_size_m": solve.mesh_size_m,
        "row_number": solve.row_number,
        "mode": mode_name,
        "status": "ok",
        "error_type": "",
        "error_message": "",
        "candidate_id": item.candidate_id,
        "source_names": ";".join(item.source_names),
        "source_support_count": item.source_support_count,
        "x_m": item.position_m[0],
        "y_m": item.position_m[1],
        "recovered_psi_v2_per_m2": item.recovered_psi_v2_per_m2,
        "recovered_field_magnitude_v_per_m": item.recovered_field_magnitude_v_per_m,
        "containing_raw_field_magnitude_v_per_m": (
            item.containing_raw_field_magnitude_v_per_m
        ),
        "adjacent_raw_field_1_v_per_m": item.adjacent_raw_field_magnitudes_v_per_m[0],
        "adjacent_raw_field_2_v_per_m": item.adjacent_raw_field_magnitudes_v_per_m[1],
        "adjacent_raw_field_jump_v_per_m": item.adjacent_raw_field_jump_v_per_m,
        "adjacent_raw_field_jump_ratio": item.adjacent_raw_field_jump_ratio,
        "distance_to_internal_facet_m": item.distance_to_internal_facet_m,
        "distance_to_internal_facet_over_mesh_h": (
            item.distance_to_internal_facet_over_mesh_h
        ),
        "distance_to_nearest_electrode_m": item.distance_to_nearest_electrode_m,
        "hessian_stability_classification": item.hessian_stability_classification,
        "hessian_stable": item.hessian_stable,
        "hessian_eigenvalue_variation_ratio": item.hessian_eigenvalue_variation_ratio,
        "recovered_psi_ratio_to_candidate_scale": (
            item.recovered_psi_ratio_to_candidate_scale
        ),
        "physically_small_recovered_psi": item.physically_small_recovered_psi,
        "facet_sensitive": item.facet_sensitive,
        "legacy_interpolation_flag": item.legacy_interpolation_flag,
        "interpolation_sensitive": item.interpolation_sensitive,
        "artifact_probability": item.artifact_probability,
        "artifact_classification": item.artifact_classification,
        "classification_reason": item.classification_reason,
        "robust_accepted": item.robust_accepted,
        "selected": item.selected,
    }
    for index in range(4):
        suffix = index + 1
        if index < len(item.hessian_steps_m):
            record[f"hessian_step_{suffix}_m"] = item.hessian_steps_m[index]
            record[f"hessian_step_{suffix}_eigenvalue_min_v2_per_m4"] = (
                item.hessian_eigenvalues_v2_per_m4[index][0]
            )
            record[f"hessian_step_{suffix}_eigenvalue_max_v2_per_m4"] = (
                item.hessian_eigenvalues_v2_per_m4[index][1]
            )
        else:
            record[f"hessian_step_{suffix}_m"] = ""
            record[f"hessian_step_{suffix}_eigenvalue_min_v2_per_m4"] = ""
            record[f"hessian_step_{suffix}_eigenvalue_max_v2_per_m4"] = ""
    return record


def _validation_records(study: Milestone8Study) -> list[dict[str, object]]:
    summary_lookup = {
        (item.mode_name, item.mapping_name, item.mesh_size_m): item
        for item in study.summaries
    }
    records = []
    for summary in study.summaries:
        records.append({"record_type": "summary", **_summary_record(summary)})
    for row in sorted(
        study.mode_rows,
        key=lambda item: (item.mode_name, item.mesh_size_m, item.row_number),
    ):
        if not np.isclose(row.mesh_size_m, 2.0e-3):
            continue
        errors = row.errors_m()
        record: dict[str, object] = {
            "record_type": "row",
            "mode": row.mode_name,
            "mapping": row.mapping_name,
            "mesh_size_m": row.mesh_size_m,
            "row_number": row.row_number,
            "status": row.status,
            "completed": row.completed,
            "exactly_three_topology": row.exactly_three_topology,
            "topology_candidate_count": row.topology_candidate_count,
            "selected_interpolation_sensitive": (
                row.selected_interpolation_sensitive
            ),
            "rejected_candidates": row.rejected_candidates,
            "total_candidates": row.total_candidates,
            "row_mean_error_m": float(np.mean(errors)) if errors.size else float("nan"),
            "row_mean_error_mm": 1.0e3 * float(np.mean(errors)) if errors.size else float("nan"),
            "row_maximum_error_m": float(np.max(errors)) if errors.size else float("nan"),
            "row_maximum_error_mm": 1.0e3 * float(np.max(errors)) if errors.size else float("nan"),
            "node_count": row.node_count,
            "triangle_count": row.triangle_count,
            "relative_free_residual": row.relative_free_residual,
            "runtime_seconds": row.runtime_seconds,
            "error_type": row.error_type,
            "error_message": row.error_message,
        }
        for match_index, match in enumerate(row.matches, start=1):
            record[f"minimum_{match_index}_error_m"] = match.distance_m
            record[f"minimum_{match_index}_error_mm"] = 1.0e3 * match.distance_m
            record[f"minimum_{match_index}_reference_x_m"] = match.reference_position_m[0]
            record[f"minimum_{match_index}_reference_y_m"] = match.reference_position_m[1]
            record[f"minimum_{match_index}_computed_x_m"] = match.computed_position_m[0]
            record[f"minimum_{match_index}_computed_y_m"] = match.computed_position_m[1]
        summary = summary_lookup[(row.mode_name, row.mapping_name, row.mesh_size_m)]
        record["validation_gate_passed"] = summary.validation_gate_passed
        records.append(record)
    return records


def _branch_record(item: MeshBranchRecord) -> dict[str, object]:
    return {
        "row_number": item.row_number,
        "branch_index": item.branch_index,
        "coarse_mesh_size_m": item.coarse_mesh_size_m,
        "fine_mesh_size_m": item.fine_mesh_size_m,
        "coarse_x_m": item.coarse_position_m[0],
        "coarse_y_m": item.coarse_position_m[1],
        "fine_x_m": item.fine_position_m[0],
        "fine_y_m": item.fine_position_m[1],
        "shift_m": item.shift_m,
        "shift_mm": 1.0e3 * item.shift_m,
        "stable_at_0_25_mm": item.stable,
        "coarse_topology_candidates": item.coarse_topology_candidates,
        "fine_topology_candidates": item.fine_topology_candidates,
    }


def _write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        raise ValueError(f"cannot write empty CSV: {path}")
    fieldnames = []
    for row in rows:
        for key in row:
            if key not in fieldnames:
                fieldnames.append(key)
    with path.open("w", newline="", encoding="utf-8") as stream:
        writer = csv.DictWriter(stream, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def _markdown_report(study: Milestone8Study) -> str:
    def at_two_mm(mode_name: str) -> ModeValidationSummary:
        return next(
            item
            for item in study.summaries
            if item.mode_name == mode_name and np.isclose(item.mesh_size_m, 2.0e-3)
        )

    old_identity = at_two_mm("old-recovered-identity")
    audit_identity = at_two_mm("audit-flag-identity")
    audit_best = at_two_mm("audit-flag-perm1324")
    robust_identity = at_two_mm("robust-identity")
    old_best = at_two_mm("old-recovered-perm1324-comparison")
    robust_best = at_two_mm("robust-perm1324")
    identity_change = _relative_change(old_identity.mean_error_m, robust_identity.mean_error_m)
    best_change = _relative_change(old_best.mean_error_m, robust_best.mean_error_m)
    branch_count = len(study.branch_records)
    stable_branches = sum(item.stable for item in study.branch_records)
    maximum_shift = max((item.shift_m for item in study.branch_records), default=float("nan"))
    fine_branches = [
        item for item in study.branch_records if item.coarse_mesh_size_m <= 1.5e-3
    ]
    stable_fine_branches = sum(item.stable for item in fine_branches)
    maximum_fine_shift = max((item.shift_m for item in fine_branches), default=float("nan"))
    robust_mesh_summaries = [
        item for item in study.summaries if item.mode_name == "robust-perm1324"
    ]
    artifact_material = bool(
        abs(identity_change) >= 0.05
        or abs(best_change) >= 0.05
        or robust_identity.completed_rows != old_identity.completed_rows
    )
    robust_topology_improved = robust_best.exactly_three_rows > old_best.exactly_three_rows
    stronger_model_class = bool(
        all(item.completed_rows == item.selected_rows for item in robust_mesh_summaries)
        and all(item.exactly_three_rows == item.selected_rows for item in robust_mesh_summaries)
        and all(item.selected_interpolation_sensitive == 0 for item in robust_mesh_summaries)
        and stable_fine_branches == len(fine_branches)
        and not robust_best.validation_gate_passed
    )
    lines = [
        "# Milestone 8 robust-minima validation report",
        "",
        "## Decision",
        "",
    ]
    if stronger_model_class:
        lines.extend(
            [
                "Robust post-processing removes the tested selected-minimum numerical confounder,",
                "while the reference gate still fails. The remaining mismatch can therefore be",
                "attributed to model class/topology with substantially stronger numerical justification.",
                "Three coarse 2.0-to-1.5 mm branch shifts exceed the reporting tolerance, so this",
                "is stronger evidence rather than a claim of exact mesh invariance.",
            ]
        )
    else:
        lines.extend(
            [
                "Robust post-processing does not satisfy every numerical precondition needed for",
                "an unqualified model-class conclusion. Failed topology, selected flags, or mesh",
                "branch motion remain visible below.",
            ]
        )
    lines.extend(
        [
            "",
            "The validation gate remains "
            + ("passed." if robust_best.validation_gate_passed else "failed."),
            "No physical FEM model, voltage convention, or default recovered-gradient behavior was changed.",
            "",
            "## Mode and mesh comparison",
            "",
            "| mode | mapping | h (mm) | completed | exactly three | mean (mm) | median (mm) | max (mm) | p95 (mm) | selected flags | rejected | gate |",
            "|:---|:---|---:|---:|---:|---:|---:|---:|---:|---:|---:|:---:|",
        ]
    )
    for item in study.summaries:
        lines.append(
            f"| {item.mode_name} | {item.mapping_name} | {1e3 * item.mesh_size_m:g} | {item.completed_rows}/{item.selected_rows} | "
            f"{item.exactly_three_rows}/{item.selected_rows} | {1e3 * item.mean_error_m:.6g} | "
            f"{1e3 * item.median_error_m:.6g} | {1e3 * item.maximum_error_m:.6g} | "
            f"{1e3 * item.percentile_95_error_m:.6g} | {item.selected_interpolation_sensitive} | "
            f"{item.rejected_candidates} | {'pass' if item.validation_gate_passed else 'fail'} |"
        )
    lines.extend(
        [
            "",
            "Audit-flag mode deliberately reuses the old coordinates and only exposes the",
            f"Milestone 7 conservative flags ({audit_identity.selected_interpolation_sensitive} identity flags;",
            f"{audit_best.selected_interpolation_sensitive} best-mapping flags, reproducing rows 3, 4, 5, and 10).",
            f"Robust identity changes the mean error by {100.0 * identity_change:.3f}% relative to old identity;",
            f"robust best-mapping changes it by {100.0 * best_change:.3f}% relative to the same-mapping old result.",
            "The changes are classified as " + ("material." if artifact_material else "not material at the documented 5% threshold."),
            "",
            "## Robust criteria",
            "",
            "Candidate sources are the legacy recovered coarse/refined search, exact zeros of the",
            "continuous recovered P1 vector field inside mesh triangles, and local lows of the raw",
            "elementwise-constant P1 field. Sources within the configured merge distance are combined",
            "with provenance retained.",
            "",
            "Robust acceptance requires a positive, stable Hessian at all valid mesh-scaled stencils",
            "and recovered |E|^2 no more than 100 times the low-candidate scale. A candidate close to",
            "an internal facet is rejected as facet-sensitive only when the adjacent raw-field jump is",
            "large and the multi-stencil Hessian is unstable. All rejected candidates remain in the CSV.",
            "The artifact probability is an uncalibrated rule score (facet 0.25, raw jump 0.20,",
            "Hessian instability 0.40, high recovered |E|^2 0.15, capped at one); no reference",
            "coordinate enters the score or robust acceptance decision.",
            "",
            "## Topology and mesh consistency",
            "",
            f"Exactly-three topology under the best mapping changes from {old_best.exactly_three_rows}/10",
            f"to {robust_best.exactly_three_rows}/10; this is "
            + ("an improvement." if robust_topology_improved else "not an improvement."),
            f"Successive-mesh branch matches stable within 0.25 mm: {stable_branches}/{branch_count}.",
            f"Maximum successive branch shift: {1e3 * maximum_shift:.6g} mm.",
            f"For transitions at h <= 1.5 mm, stability is {stable_fine_branches}/{len(fine_branches)}",
            f"with maximum shift {1e3 * maximum_fine_shift:.6g} mm.",
            "",
            "## Scientific interpretation",
            "",
            "Selected recovered-gradient artifacts are "
            + ("numerically material to the reference mismatch." if artifact_material else "not responsible for a significant fraction of the spatial mismatch."),
            "Robust selection can change candidate topology without forcing agreement with Data.txt.",
            "The conservative gate requires all rows complete, exactly-three topology for every row,",
            "mean error <= 0.25 mm, and maximum error <= 0.5 mm.",
            "",
            "Synthetic dataset generation remains unsafe because the validation gate is not met.",
            "",
            "## Reproducibility",
            "",
            f"Runtime: {study.runtime_seconds:.3f} seconds. Every FEM case ran in a fresh process.",
            "CSV coordinates and calculations use SI units; millimetre columns are presentation copies.",
        ]
    )
    return "\n".join(lines) + "\n"


def _relative_change(old: float, new: float) -> float:
    return float((new - old) / old) if np.isfinite(old) and old != 0.0 else float("nan")


