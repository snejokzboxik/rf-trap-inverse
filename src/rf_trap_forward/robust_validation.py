        robust = robust_lookup[old.row_number]
        shade = f"C{index % 10}"
        reference = 1.0e3 * old.reference_positions_m
        old_points = 1.0e3 * old.computed_positions_m
        robust_points = 1.0e3 * robust.computed_positions_m
